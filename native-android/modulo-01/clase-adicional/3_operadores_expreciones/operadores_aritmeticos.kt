/*
| Operador | Descripción | Ejemplo |
| -------- | ----------- | ------- |
| `+`      | Suma        | `a + b` |
| `-`      | Resta       | `a - b` |
| `*`      | Multiplic.  | `a * b` |
| `/`      | División    | `a / b` |
| `%`      | Módulo      | `a % b` |
 */

 fun arithmetic() {
    val a = 10
    val b = 3
    println(a + b)  // 13
    println(a - b)  // 7
    println(a * b)  // 30
    println(a / b)  // 3 (división entera)
    println(a % b)  // 1 (resto de la división)
}
